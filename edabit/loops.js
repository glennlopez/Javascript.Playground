// for loops in javascript counting 1 to 10
console.log(`for loops in javascript counting 1 to 10`);
for (let i = 1; i <= 10; i++) {
    console.log(i);
}
console.log();
console.log();


// while loops in javascript counting 1 to 10
console.log(`while loops in javascript counting 1 to 10`);
let i = 1;
while (i <= 10) {
    console.log(i)
    i++;
}
console.log();
console.log();

// do while loops in javascript
console.log(`do while loops in javascript`);
let j = 0;
do {
    //do this first
    j++;
    console.log(j);
} while (j < 10);
